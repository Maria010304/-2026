/**
 * Генерирует транскрипт с равномерным распределением фраз по длительности видео.
 */
export function generateTranscript(videoDurationSec) {
  const count = SENTENCES.length;
  const interval = videoDurationSec / count;
  return SENTENCES.map((text, idx) => ({
    id: `t-${idx}`,
    time: Math.floor(idx * interval),
    text,
  }));
}

/**
 * Возвращает индекс активной фразы для заданного времени воспроизведения.
 */
export function findActivePhraseIndex(transcript, currentTime) {
  for (let i = transcript.length - 1; i >= 0; i--) {
    if (transcript[i].time <= currentTime) return i;
  }
  return 0;
}
